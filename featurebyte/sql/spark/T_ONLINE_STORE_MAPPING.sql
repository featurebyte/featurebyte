CREATE TABLE IF NOT EXISTS ONLINE_STORE_MAPPING (
  TILE_ID VARCHAR(65535),
  AGGREGATION_ID VARCHAR(65535),
  RESULT_ID VARCHAR(65535),
  RESULT_TYPE VARCHAR(65535),
  SQL_QUERY VARCHAR(65535),
  ONLINE_STORE_TABLE_NAME VARCHAR(65535),
  ENTITY_COLUMN_NAMES VARCHAR(65535),
  IS_DELETED BOOLEAN,
  CREATED_AT TIMESTAMP
) USING delta;
