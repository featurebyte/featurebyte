CREATE TABLE IF NOT EXISTS TILE_FEATURE_MAPPING (
  TILE_ID VARCHAR(65535),
  AGGREGATION_ID VARCHAR(65535),
  FEATURE_NAME VARCHAR(65535),
  FEATURE_TYPE VARCHAR(65535),
  FEATURE_VERSION VARCHAR(65535),
  FEATURE_READINESS VARCHAR(65535),
  FEATURE_EVENT_DATA_IDS VARCHAR(65535),
  IS_DELETED BOOLEAN,
  CREATED_AT TIMESTAMP
) USING delta;
